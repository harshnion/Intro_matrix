#today we learn about matrix in python 

#It is very easy concept , we will do this with help of nested lists

#Let's take a nested list 
m = [[1,2,3],[4,5,6]]

#Here, we run loop to print each element of our matrix
for row in m:
  for col in row:
    print(col)

# By running this code we get each element of matrix as output
